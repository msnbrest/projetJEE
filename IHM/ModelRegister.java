package fr.eni.eniEncheres.IHM;

public class ModelRegister {
	
	String message ;
	

	public ModelRegister() {

	}

	public ModelRegister(String message) {
		super();
		this.message = message;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
	

}
