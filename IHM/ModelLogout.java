public class ModelLogout {
	
	String message ;
	

	public ModelLogout() {

	}

	public ModelLogout(String message) {
		super();
		this.message = message;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
	

}
